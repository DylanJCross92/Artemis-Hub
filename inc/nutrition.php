    <div class="container center">
      <h3><span class="flaticon-scale16"></span> Weight Log Tips</h3>
      <ul>
        <li>
          <h5>Know the basics:</h5> 
          <div class="body">1 pound = 3,500 calories. Healthy weight loss is 1-2 pounds a week.</div>
        </li>
        <li>
          <h5>Set small goals:</h5>
          <div class="body">Set goals that are realistic and attainable to encourage you to keep working towards your ultimate goal.</div>
        </li>
        <li>
          <h5>Weigh yourself (with a grain of salt):</h5> 
          <div class="body">Let the number on the scale serve as a guideline. When it comes to your weight, a lot comes into play—your genes, your BMI, your menstrual cycle, or even something as simple as having had a salty dinner the night before. Do not let the number on the scale define your progress! Weigh yourself at the same time of day, on the same day of the week, on the same scale, and in the same clothes.</div>
        </li>
        <li>
          <h5>Be patient:</h5> 
          <div class="body">Losing weight takes time; don&rsquo;t get discouraged. Remember that muscle weighs more than fat, but takes up far less volume.</div>
        </li>
        <li>
          <h5>Get enough sleep:</h5> 
          <div class="body">When you're sleep deprived, your body overproduces the appetite-stimulating hormone ghrelin, but under-produces the hormone leptin, which tells you when you're full. Get enough sleep to keep you feeling energized and to avoid unnecessary snacking throughout the day.</div>
        </li>
      </ul>
      <h3><span class="flaticon-restaurant23"></span> Meal Log Tips</h3>
      <ul>
        <li>
          <h5>Fuel up:</h5>
          <div class="body">Remember that food is your body&rsquo;s fuel! You need it to function and definitely to workout at your best ability. &nbsp;Use our Calorie Calculator to determine how much you should be eating to lose or maintain your weight.</div>
        </li>
        <li>
          <h5>Eat breakfast:</h5> 
          <div class="body">Always. Skipping on breakfast will make you hungry later, leading to unhealthy snacking and/or binge eating at lunch and dinner.</div>
        </li>
        <li>
          <h5>3 is magic number:</h5>
          <div class="body">Shoot for three hearty, protein-filled meals a day and only two 100-200 calorie snacks in between. </div>
        </li>
        <li>
          <h5>If you can't pronounce it—don't eat it:</h5>
          <div class="body">Cut out processed foods—these are the culprits for most health and weight issues today.</div>
        </li>
        <li>
          <h5>Hydrate:</h5> 
          <div class="body">Drink plenty of water throughout the day. It is easy to confuse thirst with hunger. So before you dive into that bag of chips, drink a glass of water first. </div>
        </li>
        <li>
          <h5>Eat protein:</h5> 
          <div class="body">Eat protein at every meal. Protein is the ultimate fill-me-up-food. It not only keeps you feeling full for longer, but it also helps build lean muscle, and encourages fat burning.</div>
        </li>
        <li>
          <h5>Treat yourself:</h5>
          <div class="body">Restricting yourself from your favorite foods will lead to sabotage. You can still enjoy these—the key is to pick healthier options when you can, and to keep everything in moderation: Bake healthier versions of your favorite cookies or have a fun-sized version of your favorite candy bar.</div>
        </li>
        <li>
          <h5>Stock up on fresh foods:</h5> 
          <div class="body">Rid your kitchen of processed foods, anything with trans fat, artificial sweeteners, and high-fructose corn syrup. Instead, stock up on healthy, fresh foods. You will be less likely to order pizza or hit the drive through if you have a kitchen fully stocked with fresh foods. We tend to crave what is easy, convenient, and often sitting right in front of us.</div>
        </li>
        <li>
          <h5>Spotlight healthy food:</h5> 
          <div class="body">Put the brightest, healthiest things on display: fruits on the countertop, vegetables at eye level in the fridge, etc.</div>
        </li>
        <li>
          <h5>Portion control:</h5> 
          <div class="body">Portion control is difficult while living in a super-sized world. To determine the right portions, use a kitchen scale and measuring cups to measure your meals for a week or two. Use smaller plates and glasses to downsize your portions. Share restaurant servings with a friend or ask to have half of it boxed up. Portion out chips or popcorn instead of eating it straight from the bag.</div>
        </li>
        <li>
          <h5>Limit alcohol:</h5> 
          <div class="body">Alcohol contains empty calories. Enjoy your favorite alcoholic beverage as an occasional treat.</div>
        </li>
        <li>
          <h5>Keep a food log:</h5> 
          <div class="body">Studies show the act of writing down what you eat and drink tends to make you more aware of what, when, and how much you're consuming -- leading you to ultimately take in fewer calories. Keep a small notebook with your or keep a log on your phone.</div> 
        </li>
        <li>
          <h5>Reward yourself:</h5> 
          <div class="body">But not with food. Reward your achievements by buying yourself a new workout outfit, a day at a spa or new cooking gear.</div>
        </li>
      </ul>
